#include<bits/stdc++.h>
using namespace std;
int main(){
    long long X ;
    long long Y;
    cin>>X>>Y ;
    cout<<X<<" + "<<Y <<" = "<<X+Y<<endl;
    cout<<X<<" * "<<Y <<" = "<<X*Y <<endl ;
    cout<<X<<" - "<<Y<<" = "<<X-Y<<endl;
return 0;
}
